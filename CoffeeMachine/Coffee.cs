namespace CoffeeMachine
{
    internal class Coffee
    {
        public int Water;
        public int Milk;
        public int Beans;
        public int Cups;
        public int Money;

        public Coffee(int water, int milk, int beans, int cups, int money) 
        {
            Water = water;
            Milk = milk;
            Beans = beans;
            Cups = cups;
            Money = money;
        }
    }
}
